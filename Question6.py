# # Q6. Suppose you conducted a one-way ANOVA and obtained an F-statistic of 5.23 and a p-value of 0.02.
# # What can you conclude about the differences between the groups, and how would you interpret these
# # results?


# # Answer - 
# Null Hypothesis (H0): The null hypothesis in a one-way ANOVA states that there are no significant differences among the group means. In other words, all groups have the same population mean.

# Alternative Hypothesis (Ha): The alternative hypothesis, on the other hand, suggests that at least one group mean is significantly different from the others.

# In your case:

# F-statistic = 5.23
# p-value = 0.02
# Here's how to interpret these results:

# If your chosen significance level (often denoted as α) is 0.05 (5%), and your p-value is less than α (0.02 < 0.05), you would reject the null hypothesis.
# Interpretation:

# Since the p-value (0.02) is less than the chosen significance level (α = 0.05), you have enough evidence to reject the null hypothesis. This means that there are statistically significant differences among the group means in your dataset.