# Q2. What are the three types of ANOVA, and in what situations would each be used?

# Answer - 

# One way Anova - One factor has two levels and both the levels are independent.

# Repeated Measured Anova - One factor with atleast two levels and both the levels are dependent.

# Factorial Anova - Two or more factor and each of which have two levels and levels would be either dependent or independent.

